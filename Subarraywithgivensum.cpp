class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(int arr[], int n, long long s)
    {
        // Your code here
        if(s==0){
            return {-1};
        }
        int start=0;
        long long t=0;
        for(int i=0; i<n; i++){
          t+=arr[i];
          while(t>s){
              t-=arr[start++];
          }
          if(t==s){
              return {start+1,i+1};
          }
        }
        return {-1};
    }
};

